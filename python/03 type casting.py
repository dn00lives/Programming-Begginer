
# type casting - convert the data type of a value to another data type.


x = 1           #int - whole number
y = 2.1         #float - number with decimal
z = "3"         #str - statements

x = str(x)      # convert int to str
y = int(y)      # convert float to int
z = float(z)    # convert str to float

print(x*6)
print(y*2)
print(z*3.1)









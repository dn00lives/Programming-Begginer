#include <iostream>

int main(){

// line space

    std::cout << '\n';

// statements
    
    std::cout << "It's really good!" << '\n';
    
    std::cout << '\n';

// integer (whole number)
    
    int x = 5;
    int age = 34;

    std::cout << x << '\n';
    std::cout << age << '\n';

    std::cout << '\n';

// double (number including decimal)
   
    double decimal = 1.50;
    double percentage = 0.50;

    std::cout << decimal << '\n';
    std::cout << percentage << '\n';

    std::cout << '\n';

// single character only
   
    char grade = '1';
    char initial = 'B';
    char dollarSign = '$';

    std::cout << grade << '\n';
    std::cout << initial << '\n';
    std::cout << dollarSign << '\n';

    std::cout << '\n';

// boolean (true or false)
    
    bool powerSwitch = false;

    std::cout << powerSwitch << '\n';

    std::cout << '\n';    

// string (objects that represents a sequence of text)
    
    std::string name = "Joshua";

    std::cout << name << '\n';

    std::cout << '\n';



// EXAMPLES ///////////////////////////////////////////////////////

    std::cout << "My name is " << name << ".";
    std::cout << '\n';
    std::cout << "I am " << age << " years old.";


    return 0;
}







